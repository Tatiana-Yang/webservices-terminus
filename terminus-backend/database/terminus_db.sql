
CREATE TABLE TER_USER
(
    id_user INTEGER NOT NULL PRIMARY KEY,
    name_user VARCHAR(25) NOT NULL,
    login_user VARCHAR(25) NOT NULL,
    password_user VARCHAR(25) NOT NULL
);

CREATE TABLE TER_QUIZ
(
    id_quiz INTEGER NOT NULL PRIMARY KEY,
    name_quiz VARCHAR(100) NOT NULL
);

CREATE TABLE TER_SCORE
(
    id_player INTEGER NOT NULL PRIMARY KEY,
    nickname VARCHAR(25) NOT NULL,
    score INTEGER NOT NULL
);

CREATE TABLE TER_QUESTION
(
    id_question INTEGER NOT NULL PRIMARY KEY,
    question VARCHAR(500) NOT NULL,
    id_tag INTEGER NOT NULL,
    id_type_question INTEGER NOT NULL,
    difficulty INTEGER NULL,
    disable INTEGER NOT NULL,
    FOREIGN KEY(id_tag) REFERENCES TER_TAG(id_tag),
    FOREIGN KEY(id_type_question) REFERENCES TER_TYPE_QUESTION(id_type_question)
);

CREATE TABLE TER_ANSWER
(
    id_answer INTEGER NOT NULL PRIMARY KEY,
    answer VARCHAR(500) NOT NULL
);

CREATE TABLE TER_TAG
(
    id_tag INTEGER NOT NULL PRIMARY KEY,
    name_tag VARCHAR(25) NOT NULL,
    color_tag VARCHAR(7) NOT NULL
);

CREATE TABLE TER_TYPE_QUESTION
(
    id_type_question INTEGER NOT NULL PRIMARY KEY,
    name_type_question VARCHAR(25) NOT NULL
);

CREATE TABLE TER_CREATE_QUIZ
(
    id_user INTEGER NOT NULL,
    id_quiz INTEGER NOT NULL,
    PRIMARY KEY(id_user,id_quiz),
    FOREIGN KEY (id_user) REFERENCES TER_USER(id_user),
    FOREIGN KEY (id_quiz) REFERENCES TER_QUIZ(id_quiz)
);

CREATE TABLE TER_GET_SCORE
(
    id_quiz INTEGER NOT NULL,
    id_player INTEGER NOT NULL,
    PRIMARY KEY(id_player,id_quiz),
    FOREIGN KEY (id_player) REFERENCES TER_SCORE(id_player),
    FOREIGN KEY (id_quiz) REFERENCES TER_QUIZ(id_quiz)
);

CREATE TABLE TER_HAS_QUESTION
(
    id_quiz INTEGER NOT NULL,
    id_question INTEGER NOT NULL,
    PRIMARY KEY(id_quiz,id_question),
    FOREIGN KEY (id_question) REFERENCES TER_QUESTION(id_question),
    FOREIGN KEY (id_quiz) REFERENCES TER_QUIZ(id_quiz)

);

CREATE TABLE TER_HAS_ANSWER
(
    id_question INTEGER NOT NULL,
    id_answer INTEGER NOT NULL,
    correct INTEGER NOT NULL,
    PRIMARY KEY(id_question,id_answer),
    FOREIGN KEY (id_question) REFERENCES TER_QUESTION(id_question),
    FOREIGN KEY (id_answer) REFERENCES TER_ANSWER(id_answer)

);

